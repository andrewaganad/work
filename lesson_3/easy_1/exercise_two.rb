# In Ruby, ! mutates the caller while ? returns a boolean value

# 1. != compares two values that are not equal. You should use it when you want
  # to show that two values are not equal.
# 2. When you put ! before something, it returns the opposite of that
  # boolean value. 
# 3. When you put ! after something, it mutates the caller.
# 4. Putting ? before something returns an error.
# 5. Putting ? after something returns a boolean value
# 6. Putting !! before something returns its boolean equivalent
